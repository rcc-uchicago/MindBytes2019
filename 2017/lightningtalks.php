                                            <p class="text-justify">Luca has always been fascinated by fundamental physics, the kind of physics that is able to change our way of looking at the surrounding world and can provide a deeper understanding of how nature works. His activities until now, have been focused on the development of two-phase noble liquids Time Projection Chamber (TPC) technology for Dark Matter direct detection. During his Ph.D., he was involved in the design, construction and operation of the SArP-2.3kg prototype, the first argon detector to have set a limit on the Weakly Interacting Massive Particles (WIMPs) interaction rate. From 2008-2015, Luca worked on the DarkSide combines argon two-phase and liquid scintillator technology. In 2015, he joined the <a href='http://xenon1t.org/' target='_blank'>XENON-1T</a> project. The experiment features a 3500kg liquid xenon detector surrounded by a large water tank to suppress the background. The XENON-1T experiment, thanks to its design, large fiducial mass, and increased sensitivity to WIMPs, will probe properties of dark matter in yet unexplored regions and will “open” a second phase of dark matter searches with multi-ton noble liquid detectors. Luca received his Ph.D. In 2005 from Universita degli Studi di Pavia, Italia.</p>
