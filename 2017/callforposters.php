                                                                    <p class="text-justify">One of the most surprising transformations in the 20th century was Communist China's embrace of market economy. To make it happen, there needs to be a huge ideological transformation in words to resolve the incompatibility between "free market" and orthodox Marxism. We attempt to model this transformation by applying computational content analysis to the full text of the communist party's mouthpiece, the People's Daily, from 1946 to 2003. There's a long debate about whether ideology has any independent explanatory power in historical processes. We found that the party's official rhetoric has progressed in a very linear and smooth fashion. Newness always comes out of old repertoires, and controversial concepts like "market economy" only became stabilized by attaching to an existing stable rhetorical subspace. Our findings echo the Weberian idea that ideology and culture should be viewed as a semi-autonomous social sphere that interacts with other social processes with its own logic.</p>
