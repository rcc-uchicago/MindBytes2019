                                                                    <p class="text-justify">Genome-wide association studies (GWAS) aim to identify genetic factors that are associated with complex traits. However, individual genetic variants have small effects, making them hard to identify. In addition, lists of individual variant associations give limited biological insights. â€œEnrichment analysesâ€ can address these problems by focusing on a set of genes, instead of individual genetic variants. Here we develop an efficient enrichment analysis method that jointly models GWAS summary statistics at millions of variants, and use it to analyse 3,913 biological pathways and 113 tissue-based gene sets in 31 human phenotypes. A key feature of our method is that inferred enrichment automatically informs new trait-associated genes. For example, enrichment in lipid transport genes suggests strong evidence for association between MTTP and low-density lipoprotein levels, whereas conventional analyses of the same data found no significant variant near this gene.</p>
