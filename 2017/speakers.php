                                        <h3 class="text-uppercase text-gray-darkgray mb-0 mt-0">Alfio M. Gliozzo</h3>
                                        <h6 class="text-gray text-gray mb-20">Research Manager, Knowledge Induction IBM T.J. Watson</h6>
                                        <p class="text-justify">Alfio Gliozzo is research leader at IBM T.J. Watson and adjunct professor at Columbia University, where he teaches cognitive computing. He was part of the research team that built Watson, the Question and Answering system that defeated the Jeopardy! grand champions of all times. His research focuses on Knowledge Induction from text using Deep Learning, Natural Language Processing, Semantic Web and Reasoning techniques.</p>
                                        <p class="text-justify"><b>IBM Watson</b> is arguably one of the most advanced form of AI in the cognitive computing marketplace, as demonstrated by the historic exhibition match on the television quiz show Jeopardy!. Since then, cognitive technology is becoming pervasive in information systems. A wide range of cognitive capabilities, ranging from machine translation to image recognition have been made available on cloud to any developer, opening a new market for cognitive computing. However, perception is just one of the many fundamental abilities that characterize cognition. The new challenge for IBM Watson and for the whole industry will be to help human professionals doing better decisions in their own areas of interest. The next generation AI systems will show professional level of competence, leveraging deep domain knowledge accumulated over decades. To this aim, deep learning based solutions will be just one of many ingredients, knowledge representation and reasoning being the other two pillars. In this talk I’ll describe progress made by my team at IBM Research and envision new research directions.</p>
